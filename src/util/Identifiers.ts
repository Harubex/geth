/**
 * Returns a random first name from the list available.
 */
export function randomFirstName(): string {
    return firstNames[randomInt(firstNames.length)];
}

/**
 * Returns a random last name from the list available.
 */
export function randomLastName(): string {
    return lastNames[randomInt(lastNames.length)];
}

/**
 * Generates and returns a creep name. This is guaranteed to be unique.
 */
export function uniqueName(): string {
    let newName = "";
    do {
        newName = `${randomFirstName()} ${randomLastName()}`;
    } while (Object.keys(Game.creeps).find((name) => name === newName));
    return newName;
}

/**
 * Generates and returns a globally-unique identifier (GUID).
 */
export function uniqueId(): string {
    return "xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx".replace(/[xy]/g, (ch: string) => {
        const r = Math.random() * 16 | 0;
        const v = ch === "x" ? r : (r & 0x3 | 0x8);
        return v.toString(16);
    });
}

function randomInt(max: number): number {
    return Math.round(Math.random() * max);
}
